public class Pasien {
    private String idPasien;
    private String namaPasien;

    public Pasien(String idPasien, String namaPasien) {
        this.idPasien = idPasien;
        this.namaPasien = namaPasien;
    }

    public String getIdPasien() {
        return idPasien;
    }

    public String getNamaPasien() {
        return namaPasien;
    }
}
